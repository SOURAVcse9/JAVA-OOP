public class IncrementDecrement {
    
    public static void main(String[] args){
        int a=10;
        System.out.println(a);
        System.out.print("post increment.................");//10(11)
        System.out.println(a++); 
        System.out.print("pree increment.................");//12
        System.out.println(++a);
        System.out.print("post decrement.................");//12 (11)
        System.out.println(a--);
        System.out.print("pree increment.................");//10
        System.out.println(--a);

   
    }
}
